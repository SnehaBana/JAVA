public class EvenOdd1 {

    public static void evenoddm(int n) {
        if (n % 2 == 0) {
            System.out.println("num is even");
        } else

        {
            System.out.println("num is odd");
        }

    }

    public static void main(String[] args) {
        evenoddm(5);
    }

}
